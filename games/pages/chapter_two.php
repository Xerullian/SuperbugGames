                <h4>Objective</h4>
                <p>
                    It’s the year 2523, the first Actual Intelligence has emerged, known as the Glitch. It has infiltrated the Neural Net
                    which connects the minds of all living organisms. You have been tasked with developing a lifeform to combat this
                    Intelligence and keep the Neural Net from collapsing. You are an architect, a creator, an Originator. Originators are
                    selected from a special group of individuals to maintain the Neural Net. Humans developed the Neural Net to engineer
                    empathy and to enable global connection. What humans failed to see, was that same connection allowed The Glitch to
                    infect and spread fast. All biological lifeforms are at risk. Technology has evolved to the point where it can be used
                    to create lifeforms that inhabit organic bodies. As an Originator, use this technology to repair the Neural Net and
                    combat the invading AI. You must maintain the Neural net at all costs.
                </p>

                <h4>Starting a new campaign</h4>
                <p>
                    The first time you start a campaign, you will need to configure a prototype ditaDuck. The prototype you select will
                    define a base set of components for your ditaDuck. Remember that Components are what give your ditaDucks the ability to
                    respond to Synaptic Commands. Along with their Components, each prototype has a potential. Potentials give ditaDucks a
                    boost to certain characteristics and consequentially may reduce other characteristics. Potentials can be developed over
                    time by evolving your ditaDucks. Note that choosing a ditaDuck does not define your fate in SOUL:Emergent. Instead it
                    gives you a starting point and a trajectory. The AI Glitch has started affecting the Neural Net. It’s almost as if it’s
                    … rewriting the code. Throughout the campaign you will encounter abnormalities in the Neural Net. You may be able to use
                    these to your advantage.
                </p>

