        <div class="w3-content w3-justify  w3-padding-16" id="acronym">
            <h2 class="">The SOUL Acronym</h2>
            <hr style="width:200px" class="w3-opacity">
            <p>
                <strong>SOUL</strong> is an acronym that stands for <strong>SYNAPTIC ORBITAL UNIFICATION LOGIC</strong>. This
                acronym was designed to help define the different game play elements in <strong>SOUL:Emergent</strong>. Each part
                of <strong>SOUL</strong> relates to a different game play mechanic. The different mechanics are designed to
                seamlessly work with each other so that they make a whole. That is to say, not having access to a mechanic would
                be disastrous. Exploring each mechanic will enable you to systematically progress your way through the game world.
                The different mechanics will be discussed in the next section <a>UNDERSTANDING THE PARTS OF SOUL</a>.
            </p>
        </div>

