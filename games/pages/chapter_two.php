                <h4>Communicating with your ditaDucks</h4>
                <p>
                    Your Core is where you issue Synaptic Commands to your ditaDucks. Due to the way the Neural Net was constructed, these
                    commands are able to travel through connections and influence targets. However, the connections are limited in range.
                    That range is defined as your Orbital Sphere. The Orbital Sphere can be viewed on your screen as a circular highlight.
                    It can be toggled on or off. ditaDucks and other objects outside your Orbital sphere are shielded from your influence.
                    It is possible to expand your Orbital Sphere by constructing Relays. Relays are points of contact that receive and
                    retransmit Synaptic Commands. One a Relay is placed it will connect with your Core and increase the range of your
                    influence.
