                <p>
                    Keeping the Nuerual Net operational is the main objective of SOUL:Emergent. This is done by repairing sectors and
                    joining Networks. Once you repair a sector, a firewall will be installed by the Neural Net's operating system. This will
                    safeguard that sector from further intrusion. At this point, the Glitch is locked out. It is not required to join all networks.
