            <h2 class="">Parts of The Game</h2>
            <hr style="width:200px" class="w3-opacity">

                <div>
                    <h5>Units – Entities that can be controlled</h5>
                    <p>
                        ditaDucks (1): These are the entities that you control as the player. They can take many shapes and sizes. They have
                        many different skills sets and interact with the environment in unique ways.
                    </p>

                    <p>
                        Glitches (2): Fraudulent ditaDucks.
                    </p>
                </div>

                <div>
                    <h5>Environment – Places that can be visited</h5>
                    <p>
                        Core (3): A part that needs to be repaired.
                        Sectors: Sectors are areas in a NETWORK that need to be repaired. They could have a core that has fragmented, a
                        collection of segments that have faulted, and or a transit that has halted, and all while you are being assaulted. Yes,
                        there are fraudulent ditaDucks in each sector called Glitches. The GLITCHES have been attempting to hijack the Networks
                        for eons.
                    </p>

                    <p>
                        Networks: DITA passes through the networks constantly. While it travels, dita is constantly degrading until it becomes
                        CRUX. GLITCHES are attracted to the crux and feed off it while infecting the networks.
                    </p>
                </div>

                <div>
                    <h5>Resources – Things that can be collected or consumed</h5>
                    <p>
                        Dita (4): No one truly knows what dita is, but ditaDucks love it! Dita appears in many forms and has different shapes
                        and colors. The environment seems to influence its characteristics. When ditaDucks consume dita they are able to change
                        their size.
                    </p>

                    <p>
                        Crux (5): When dita degrades it becomes crux. Crux is what attracts glitches. When ditaDucks consume Crux, they are
                        altered in various ways. The ditaDucks have even been known to glow!
                    </p>
