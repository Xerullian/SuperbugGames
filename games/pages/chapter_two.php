            <h2 class="">Emergent Gameplay</h2>
            <hr style="width:200px" class="w3-opacity">
            <p>
                A great General once said, “Emerge and be VICTORIOUS!” While this may be true, SOUL:Emergent defines EMERGENT as much
                more. Emergence is a theme revisited throughout the entire game. You are given a box of tools and an objective. You use
                those tools how you see fit to achieve those means. That what Emergent means in SOUL:Emergent.

                <b>The Role of Emergent Gameplay in SOUL:Emergent</b>
                You have a set of tools. You have an objective. How do you meet your objective? That is a matter entirely up to you! Of
                course, you’ll be given some hints. In fact, you’ll be given a lesson on how you might attack your objective in the
                LEARNING TO PLAY SOUL:EMERGENT tutorials. You start off with a tiny ditaDuck. It is just coming to terms that it is
                alive. It knows how to consume CRUX.
            </p>
